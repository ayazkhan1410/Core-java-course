package second_chpter;

public class lect9_type_casting {
    public static void main(String[] args) {
        //this is called type casting
        /*
         * int a = 5;
         * double sum;
         * sum = 14.5 + 5;
         * System.out.println(sum);
         */
        /*
         * int a = 6;
         * float sum;
         * sum = a + 4.7f;
         * System.out.println("The sum of a is : " + sum);
         */
        double num1 = 5.66d;
        int num2 = (int) num1;

        System.out.println(num2);

    }

}
